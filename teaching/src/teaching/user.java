package teaching;

public class user
{
	private String name; 
	private String email; 
	
	
	public user(String inname, String inemail){
		name = inname; 
		email = inemail;
		
		
	}
	
	public String getname(){
		
		return name; 
		
	}
	
	public String getemail() {
		
		
		return email; 
	}
	
	
	
	
	
}
